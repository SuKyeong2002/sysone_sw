package operation;

public class OpTest9 {
	public static void main(String[] args) {
		int a = 10;
		
		a += 2; // 축약형 연산: a = a + 2;
		System.out.println(a);
		
		a -= 2; // 축약형 연산: a = a - 2;
		System.out.println(a);
		
		a *= 2; // 축약형 연산: a = a * 2;
		System.out.println(a);
		
		a /= 2; // 축약형 연산: a = a / 2;
		System.out.println(a);
		
		a %= 2; // 축약형 연산: a = a % 2;
		System.out.println(a);
	}
}
